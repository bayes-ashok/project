package model;


public class settingsModel {
    private String fname, lname, number, email, password, confirmPass,currentEmail;
    public settingsModel(String fname, String lname, String number, String email, String password, String confirmPass) {
        this.fname=fname;
        this.lname=lname;
        this.number=number;
        this.email=email;
        this.password=password;
        this.confirmPass=confirmPass;
    }

    public settingsModel(String currentEmail) {
        this.currentEmail=currentEmail;
    }

    public String getCurrentEmail() {
        return currentEmail;
    }

    public void setCurrentEmail(String currentEmail) {
        this.currentEmail = currentEmail;
    }

    public String getFname() {
        return fname;
    }

    public void setFname(String fname) {
        this.fname = fname;
    }

    public String getLname() {
        return lname;
    }

    public void setLname(String lname) {
        this.lname = lname;
    }

    public String getNumber() {
        return number;
    }

    public void setNumber(String number) {
        this.number = number;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getConfirmPass() {
        return confirmPass;
    }

    public void setConfirmPass(String confirmPass) {
        this.confirmPass = confirmPass;
    }
    
}
