    public registrationModel getValue(){
        String name=nameField.getText();
        String num=numField.getText();
        String email=emailField.getText();
        String fName=fNameField.getText();
        String mName=mNameField.getText();
        String address=addressField.getText();
        String collegeName=collegeField.getText();
        String roomNum=(String)roomNumber.getSelectedItem();
        String status="living";
        registrationModel obj=new registrationModel(name, num,email,fName,mName,address,collegeName,roomNum,status);
        return obj;
    }

