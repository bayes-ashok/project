    private void registerButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registerButtonActionPerformed
        // TODO add your handling code here:
        String fname = fNameField.getText();
        String lname= lNameField.getText();
        String number=numberField.getText();
        String email = emailField.getText();
        char[] passwordChars = passwordField.getPassword();
        String pass = new String(passwordChars);
        String confirmpassword=confirmPasswordField.getText();
        int length = number.length();
        boolean check1 = Pattern.matches(".*\\.com.*", email);
        boolean check2 = Pattern.matches(".*@.*", email);
        String sec1=securityQuestion1.getText();
        String sec2=securityQuestion2.getText();
        try{
            Connection connect = DbConnection.connect();
            PreparedStatement st = connect.prepareStatement("Select email from signUp");
            ResultSet res = st.executeQuery();
            int i=0;
            while (res.next()) {
                String emailValue = res.getString("email");
                if(emailValue.equals(email)){
                    i++;
                }
            }
            if(fname.isEmpty() || lname.isEmpty() || number.isEmpty() || email.isEmpty() || pass.isEmpty() || confirmpassword.isEmpty() || sec1.isEmpty() || sec2.isEmpty()){
                JOptionPane.showMessageDialog(this,"One or more field empty!");
            }else if(!(pass.equals(confirmpassword))){
                JOptionPane.showMessageDialog(this,"Password does not match");     
            }else if(length!=10){
                JOptionPane.showMessageDialog(this,"Phone Number Invalid");  
            }else if(!(check1 && check2)){
                JOptionPane.showMessageDialog(this,"Invalid Email");
            }else if(i!=0){
                JOptionPane.showMessageDialog(this,"Email already taken");
            }
            else{
                userSignUp(fname,lname,number,email,pass,confirmpassword,sec1,sec2);
                setVisible(false);
                new loginDormify().setVisible(true);
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_registerButtonActionPerformed

