    private void userSignUp(String fname, String lname, String phoneNumber, String email, String password, String confirmPassword, String ques1,String ques2) {
        Connection connect = DbConnection.connect();
        if(connect != null){
            try{      
                PreparedStatement st = connect.prepareStatement("INSERT INTO signUp (fname, lname, number, email, password, confirmpassword, securityQuestion1, securityQuestion2) VALUES (?, ?, ?, ?, ?, ?,?,?)", Statement.RETURN_GENERATED_KEYS);
                st.setString(1,fname);
                st.setString(2,lname);
                st.setString(3,phoneNumber);
                st.setString(4,email);
                st.setString(5,password);
                st.setString(6,confirmPassword);
                st.setString(7,ques1);
                st.setString(8,ques2);
                st.executeUpdate();
                JOptionPane.showMessageDialog(this, "User data inserted");
            }catch(Exception e){
               JOptionPane.showMessageDialog(this, e.getMessage());
            }
        }else{
            System.out.println("The connection not available");
        }
    }
